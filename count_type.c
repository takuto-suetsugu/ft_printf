#include "ft_printf.h"

int	count_type(char *str)
{
	if (*str && (*str == 'c' || *str == 's' || *str == 'p' || *str == 'd'
			|| *str == 'i' || *str == 'u' || *str == 'x' || *str == 'X'
			|| *str == '%'))
		return (1);
	return (0);
}
