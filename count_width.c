#include "ft_printf.h"

int	count_width(char *str)
{
	int	i;

	i = 0;
	if (*str && *str == '*')
		return (1);
	while (*str && ('0' <= *str && *str <= '9'))
	{
		str++;
		i++;
	}
	return (i);
}
